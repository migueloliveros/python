# Programa para calcular el área de un triángulo

# 1. Toma de datos
base = float(input("Base del triángulo:"))
altura = float(input("Altura del triángulo:"))

# 2. Cálculo
area = base * altura / 2

# 3. Mostrar resultados
print("Area del triángulo:", area)

